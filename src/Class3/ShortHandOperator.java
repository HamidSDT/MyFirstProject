package Class3;

public class ShortHandOperator {
    public static void main(String[] args) {

        int number = 10;
                               // number= number +10;       also can do it this way
        number +=10;

        System.out.println(number);

        int number2 = 20;
        number2 -=10;   //        number2 = number2 - 10;      also can do it this way
        System.out.println(number2);

        int number3 = 100;
        number3 /=10;        // number3 = number3 / 10;      also can do it this way

        System.out.println(number3);





    }
}
