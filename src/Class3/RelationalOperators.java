package Class3;

public class RelationalOperators {
    public static void main(String[] args) {

        // relational operators always come with boolean result . either true or false

        int number = 10;
        int number2 = 20;
        System.out.println(number+number2);

        boolean result = 10>20;
        System.out.println(result); // this will execute a false result . because 10 is not greater than 20.

        System.out.println(10>20);  // is 10 greater than 20? --.> false
        System.out.println(10<20);  // is 10 less than 20 ? --->  true
        System.out.println(10!=20); // is 10 not equal to 20? ----> true
        System.out.println(10==20); // is 10 equal to 20? --- > false
        System.out.println(10>=20); // is 10 equal or greater  to 20? --- > false
        System.out.println(10<=20); // is 10 equal or less  to 20? --- > true






    }
}
