package Class6;

public class IncrementOperator {
    public static void main(String[] args) {

        int number = 10;
        number=number+1;             // basic way to add or increment
        System.out.println(number);
        number+=1;                  // shorthand operator
        System.out.println(number);
        number++;                   // also can add it this way
        System.out.println(number);








    }
}
