package Class5;

public class SwichCaseDemoCharType {
    public static void main(String[] args) {

       char gender = 'm';

       switch (gender){
           case 'M':
           case 'm' :   System.out.println("Male");break;
           case 'F':
           case 'f' :   System.out.println("Female"); break;
           default: System.out.println("Not specified ");
       }









    }
}
